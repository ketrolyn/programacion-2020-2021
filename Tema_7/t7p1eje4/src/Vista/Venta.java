    public void validarUnidades(String cbProductos, String tfUnidades) {
        try {
            if (tfUnidades.isEmpty()) {
                throw new CampoVacio();
            }
            for (int x = 0; x < tfUnidades.length(); x++) {
                if (Character.isAlphabetic(x)) {
                    throw new FormatoIncorrecto();
                }
            }
            Controlador.validarCuantidadEnTienda(this.tfUnidades.getText());

        } catch (CampoVacio cv) {
            showMessageDialog(this, "El campo 'Unidades' no puedes está vacío");
        } catch (FormatoIncorrecto fi) {
            showMessageDialog(this, "El campo 'Unidades' solo admite numeros entero");
        }
    }

