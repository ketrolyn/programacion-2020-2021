    private void tfUnidadesFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfUnidadesFocusLost
        boolean esMayor = Controlador.comprobarUnidades(this.tfUnidades.getText());

        if (esMayor != false) {
            float importe = Controlador.calcularImporte(this.tfUnidades.getText());
            this.tfImporte.setText(Float.toString(importe) + " € ");
        }
        else
            mensajeVentaNoValida();
    }//GEN-LAST:event_tfUnidadesFocusLost

